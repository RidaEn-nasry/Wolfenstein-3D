#include "libft.h"

void	ft_dumpbin_int( int num )
{
	int	i;

	while (num)
	{
		i = num % 2;
		ft_putchar_fd(i + '0', 1);
		num /= 2;
	}
	ft_putchar_fd('\n', 1);
}
